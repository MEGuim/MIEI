public class RedeAnualNotFoundException extends Exception {

	/**
	 * Creates a new instance of <code>RedeAnualNotFoundException</code> without detail message.
	 */
	public RedeAnualNotFoundException() {
	}

	/**
	 * Constructs an instance of <code>RedeAnualNotFoundException</code> with the specified detail message.
	 *
	 * @param msg the detail message.
	 */
	public RedeAnualNotFoundException(String msg) {
		super(msg);
	}
}
